 private void init(){
        titulo = "JAFH";
        setLocationRelativeTo(null);
        setTitle(titulo);
        directorio = new Directory(this, jtCodigo, titulo, ".JAFH");
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent e){
                directorio.Exit();
                System.exit(0);
            }
        });
        Functions.setLineNumberOnJTextComponent(jtCodigo);
        timerKeyReleased = new Timer((int) (1000 * 0.3), (ActionEvent e) -> {
            timerKeyReleased.stop();
            colorAnalysis();
        });
        Functions.insertAsteriskInName(this, jtCodigo, () -> {
            timerKeyReleased.restart();
        });
        tokens = new ArrayList<>();
        errores = new ArrayList<>();
        colortexto = new ArrayList<>();
        idProd = new ArrayList<>();
        identificadores = new HashMap<>();
        Functions.setAutocompleterJTextComponent(new String[]{"False", "None", "True", "and", "print","if","else if"}, jtCodigo, () -> {
            timerKeyReleased.restart();
        }); 
    }
    
