    private void imprimirConsola(){
        int sizeErrors = errores.size();
        if(sizeErrors>0){
            Functions.sortErrorsByLineAndColumn(errores);
            String strErrores = "\n";
            for(ErrorLSSL error : errores){
                String strError = String.valueOf(error);
                strErrores += strError + "\n";
            }
            jtaOutputConsole.setText("Compilacion terminada... \n"+strErrores+"\nLa compilacion termino con errores");
        }else{
            jtaOutputConsole.setText("Compilacion terminada exitosamente...");
        }
    }   
    
    
