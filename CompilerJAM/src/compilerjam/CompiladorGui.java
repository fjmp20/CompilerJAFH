    private void clearFields(){
        Functions.clearDataInTable(tblTokens);
        jtaOutputConsole.setText("");
        tokens.clear();
        errores.clear();
        idProd.clear();
        codeHasBeenCompiled = false;
    }
    
    private void compile(){
        clearFields();
        analisisLexico();
        llenartablatokens();
        analisisSintactico();
        analisisSemantico();
        imprimirConsola();
        codeHasBeenCompiled = true;
    }
    
